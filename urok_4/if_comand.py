'''If'''
'''команда if позволяет проверить текущее состояние прогнраммы и выбрать долнейшее действие в зависимости от результата'''

names = ['дэвид', 'мия', 'анна']

for name in names:
    if name == 'мия':
        print(name.upper())
    else:
        print(name.title())

'''В команде if центральное место занимает выражение. Результатом которого явл. логическая истинна (True)
или логичкская ложь(False). Это вырожение называется условием. В результат равен True то код выполнится.
Если False то весь код следующий за if игнорируется'''

'''Проверка равенства'''
name_1 = 'олег'
print(name_1 == 'олег') # Два знака равно это проверка на равенство. Мы спрашивем равно ли значение в переменной name_1 строке 'олег'

name_2 = 'Анна'
print(name_2.lower() == 'анна')



'''Проверка на неравенство'''
'''Проверка на то что два значения различны проверяется знаком != . Восклицательный знак означает отрицание'''

name_3 = 'Николай'

if name_3 != 'олег':
    print('Это True')

