'''Проверка чисел'''
age = 20
print(age == 21)
print(age > 21)
print(age < 21)
print(age >= 21)
print(age <= 21)


print('------------')


'''Проверка нескольких условий'''
'''and'''
'''Чтобы проверить что два условия являются истинной одновременно. Их надо обьеденить ключевым словом and.
Если оба условия истинны то и все выражение истинно. Если одно из условий ложно то все выражение ложное'''

age_0 = 25
age_1 = 18
print(age_0 >= 21 and age_1 >= 21)
age_1 = 24
print(age_0 >= 21 and age_1 >= 21)

print('------------')

'''or'''
'''Ключевое слово or  также позволяет проверить несколько условий. Но рзультат общей проверки будет истинным
когда хотябы одно из условий является истинной или оба условия'''
age_0 = 25
age_1 = 18
print(age_0 >= 21 or age_1 >= 21)
age_0 = 20
print(age_0 >= 21 or age_1 >= 21)


print('------------')

